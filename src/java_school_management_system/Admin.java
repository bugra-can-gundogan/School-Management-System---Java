    public void GetCoursesTableReady(){
        ArrayList<CourseModel> courseList = DatabaseContext.GetAllCourses("");
        DefaultTableModel tblModel = (DefaultTableModel) CourseTable.getModel();
        for(int i = 0; i<courseList.size(); i++){
            String tbData[] = {String.valueOf(courseList.get(i).getCourseID()),courseList.get(i).getCourseName(), courseList.get(i).getCourseCode(), courseList.get(i).getCourseDepartment(), courseList.get(i).getCourseFaculty(), courseList.get(i).getCourseDay(), String.valueOf(courseList.get(i).getCourseHour()), String.valueOf(courseList.get(i).getCourseCredit()), courseList.get(i).getCourseTutor(), String.valueOf(courseList.get(i).getCourseTutorID())};
            tblModel.addRow(tbData);
        }
    }
    
