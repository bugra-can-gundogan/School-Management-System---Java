    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int cId = -1;
        String cName = cNameTf.getText();
        String cCode= cCodeTf.getText();
        String cDepartment= cDepTf.getText();
        String cFaculty= cFacTf.getText();
        String cTutor= cTutorNameTf.getText();
        String cDay= CDayTf.getText();
        String cHour= CHourTf.getText();
        int cCredit= Integer.parseInt(CCreditTf.getText());
        String cExamDate = "-";
        String cAnnouncement = "-";
        int cTutorId = Integer.parseInt(cTutorIDTf.getText());
        CourseModel cModel = new CourseModel(cId, cName,cCode, cDay, Integer.parseInt(cHour), cTutor, cDepartment, cFaculty, cCredit, cExamDate, cAnnouncement,cTutorId);
        if(DatabaseContext.AddCourse(cModel)){
            JOptionPane.showMessageDialog(null, "Course added successfully.");
            RefreshTable(CourseTable);
            GetCoursesTableReady();
            return;
        }else{
            JOptionPane.showMessageDialog(null, "Addition failed.");
            return;
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void RemoveSupBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RemoveSupBtnActionPerformed
        // TODO add your handling code here:
        if(SupTable.getSelectedRow() == -1){
            JOptionPane.showMessageDialog(null, "Please select a row first.");
            return;
        }
        int column = 0;
        int row = SupTable.getSelectedRow();
        String value = SupTable.getModel().getValueAt(row, column).toString();
        int x = Integer.parseInt(value);
        DatabaseContext.DisapproveSupervisor(x);
        JOptionPane.showMessageDialog(null, "Supervisor removed.");
        RefreshTable(SupTable);
        GetSupTableReady();
        RefreshTable(SupApplyTable);
        GetSupApplyTableReady();
    }//GEN-LAST:event_RemoveSupBtnActionPerformed

    private void ConfirmSupervisorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ConfirmSupervisorActionPerformed
        // TODO add your handling code here:
        if(SupApplyTable.getSelectedRow() == -1){
            JOptionPane.showMessageDialog(null, "Please select a row first.");
            return;
        }
        int column = 0;
        int row = SupApplyTable.getSelectedRow();
        String value = SupApplyTable.getModel().getValueAt(row, column).toString();
        int x = Integer.parseInt(value);
        Boolean y = DatabaseContext.ApproveSupervisor(x);
        JOptionPane.showMessageDialog(null, "Approved!");
        RefreshTable(SupApplyTable);
        GetSupApplyTableReady();
        RefreshTable(SupTable);
        GetSupTableReady();
    }//GEN-LAST:event_ConfirmSupervisorActionPerformed

